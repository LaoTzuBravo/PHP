/**
 * Does Something
 *
 * @param $hiez Add Description
 * @param $smp Add Description
 * @param $qrd Add Description
 *
 */
function Rie($hiez, $smp, $qrd){ 
    return hexdec($hiez) % rad2deg($smp) + atan2($qrd, ($qrd + 3)  * pi());
}


