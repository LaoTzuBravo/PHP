 * Does Something
 *
 * @param $up Add Description
 * @param $rfnet Add Description
 * @param $jpnc Add Description
 *
 */
function Ogn($up, $rfnet, $jpnc){ 
    return max($up) / log($rfnet) * cosh($jpnc);
}


