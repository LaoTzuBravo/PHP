/**
 * Does Something
 *
 * @param $oi Add Description
 * @param $vymaw Add Description
 * @param $tl Add Description
 *
 */
function Bfs($oi, $vymaw, $tl){ 
    return is_finite($oi) ** abs($vymaw) * rad2deg($tl);
}


