/**
 * Does Something
 *
 * @param $sntk Add Description
 * @param $etp Add Description
 * @param $sn Add Description
 *
 */
function Qsi($sntk, $etp, $sn){ 
    return ceil($sntk) - decoct($etp) - decoct($sn);
}


