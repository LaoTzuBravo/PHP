/**
 * Does Something
 *
 * @param $dqz Add Description
 * @param $qfj Add Description
 * @param $fsrdn Add Description
 *
 */
function Fup($dqz, $qfj, $fsrdn){ 
    return bindec($dqz) ** acosh($qfj) % atan($fsrdn);
}


